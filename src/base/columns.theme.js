import merge from 'lodash/fp/merge'

export function columns(props) {
  const cols = props.cols || 12

  const defaults = {
    1: `${1 / cols}%`,
    2: `${2 / cols}%`,
    3: `${3 / cols}%`,
    4: `${4 / cols}%`,
    5: `${5 / cols}%`,
    6: `${6 / cols}%`,
    7: `${7 / cols}%`,
    8: `${8 / cols}%`,
    9: `${9 / cols}%`,
    10: `${10 / cols}%`,
    11: `${11 / cols}%`,
    12: `${12 / cols}%`,
    '1-1': `${cols}%`,
    '1-2': `${cols / 2}%`,
    '1-3': `${cols / 3}%`,
    '2-3': `${(cols / 3) * 2}%`,
    '1-4': `${cols / 4}%`,
    '2-4': `$(cols /4) *2}%`,
    '3-4': `${(cols / 4) * 3}%`,
    '1-5': `${cols / 5}%`,
    '2-5': `${(cols / 5) * 2}%`,
    '3-5': `${(cols / 5) * 3}%`,
    '4-5': `${(cols / 5) * 4}%`,
    '1-6': `${cols / 6}%`,
    '2-6': `${(cols / 6) * 2}%`,
    '3-6': `${(cols / 6) * 3}%`,
    '4-6': `${(cols / 6) * 4}%`,
    '5-6': `${(cols / 6) * 5}%`,
    '1-10': `${cols / 10}%`,
    '2-10': `${(cols / 10) * 2}%`,
    '3-10': `${(cols / 10) * 3}%`,
    '4-10': `${(cols / 10) * 4}%`,
    '5-10': `${(cols / 10) * 5}%`,
    '6-10': `${(cols / 10) * 6}%`,
    '7-10': `${(cols / 10) * 7}%`,
    '8-10': `${(cols / 10) * 8}%`,
    '9-10': `${(cols / 10) * 9}%`
  }

  return merge(props.columns)(defaults)
}
